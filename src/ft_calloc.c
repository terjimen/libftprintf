#include "libft.h"
#include "ft_printf.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*str;

	str = malloc(count * size);
	if (str == NULL)
		return (str);
	ft_bzero(str, size * count);
	return (str);
}
